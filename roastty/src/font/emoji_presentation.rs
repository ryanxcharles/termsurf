/// Whether `cp` defaults to emoji presentation per the UCD
/// `Emoji_Presentation` property. Binary search over the sorted ranges.
pub(crate) fn is_emoji_presentation(cp: u32) -> bool {
    // The last range whose start is <= cp; if cp falls within its end, it matches.
    let i = EMOJI_PRESENTATION.partition_point(|&(start, _)| start <= cp);
    i > 0 && cp <= EMOJI_PRESENTATION[i - 1].1
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn is_emoji_presentation_known() {
        // Emoji_Presentation = Yes.
        for cp in [0x231A, 0x26BD, 0x1F004, 0x1F600] {
            assert!(is_emoji_presentation(cp), "{cp:#x} should be emoji");
        }
        // Emoji but text-default (no Emoji_Presentation), and non-emoji.
        for cp in [0x2764, 0x263A, 0x41, 0x2500, 0x10_FFFF] {
            assert!(!is_emoji_presentation(cp), "{cp:#x} should be text");
        }
        // Boundaries of the first range.
        let (first_start, first_end) = EMOJI_PRESENTATION[0];
        assert!(is_emoji_presentation(first_start));
        assert!(is_emoji_presentation(first_end));
        assert!(!is_emoji_presentation(first_start - 1));
    }

    #[test]
    fn emoji_presentation_table_sorted() {
        assert_eq!(EMOJI_PRESENTATION.len(), 296);
        for &(start, end) in EMOJI_PRESENTATION {
            assert!(start <= end, "range {start:#x}..={end:#x} is inverted");
        }
        for w in EMOJI_PRESENTATION.windows(2) {
            // Sorted and strictly non-overlapping (a gap between ranges).
            assert!(
                w[0].1 < w[1].0,
                "ranges {:#x?} and {:#x?} overlap/touch",
                w[0],
                w[1]
            );
        }
    }
}
